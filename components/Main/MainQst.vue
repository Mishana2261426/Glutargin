<script setup>
import { reactive, ref } from 'vue'

const form = reactive({
    name: '',
    email: '',
    question: '',
    agree: false
})

const loading = ref(false)
const success = ref(false)
const error = ref('')

const submitForm = async () => {
    error.value = ''
    success.value = false

    if (!form.name || !form.email || !form.question) {
        error.value = 'Заполните все поля'
        return
    }

    if (!form.agree) {
        error.value = 'Необходимо согласие на обработку данных'
        return
    }

    loading.value = true

    try {
        const res = await $fetch(
            'https://systemotekhnik.ru/glut/wp/wp-json/forms/v1/submit',
            {
                method: 'POST',
                body: {
                    name: form.name,
                    email: form.email,
                    question: form.question
                }
            }
        )

        if (res?.success) {
            success.value = true

            form.name = ''
            form.email = ''
            form.question = ''
            form.agree = false
        } else {
            error.value = 'Ошибка отправки'
        }
    } catch (e) {
        console.error(e)
        error.value = 'Ошибка сервера'
    } finally {
        loading.value = false
    }
}
</script>


